# 1. Proposição ou Setença (E/Também --> &)  

6 == 6
6 == 6 & 7 == 8
6 == 6 & 7 != 8
5 > 3 & 5 < 8

# 2. Proposição ou Setença (Ou --> |)

6 == 6
7 == 8
7 == 8
6 == 6 | 7 == 8
5 > 3 | 5 < 8

# 3. Preposição ou Setença (Negação --> !Setença)

